# This program will allow the user to input the value of a circles radius in order to output the area of said circle.
pi = 3.14159
# The user may change the unit of measurment required where it says the word "inches" in the line of code below.
radius_in_inches = float(input("Please type in the Radius of the circle in the given box:"))
area_of_circle = pi * radius_in_inches**2
# The user may change the unit of measurment required where it says the word "inches" in the line of code below.
print("The area of the circle is",area_of_circle, "inches squared.")